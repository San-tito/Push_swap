#include "push_swap.h"

int	main(int argc, char **argv)
{
	t_stack	*a;
	t_stack	*b;

	a = NULL;
	b = NULL;
	if (argc < 3)
		return (0);
	setup_stack(&a, argv, argc);
	insertion_sort(&a, &b);
	clear_stack(&a);
	clear_stack(&b);
	return (0);
}
